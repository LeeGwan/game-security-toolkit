// XOR encrypt/decrypt DLL
BYTE* Xor_File::Xor_dll(BYTE* original, std::streampos fileSize)
{
    
    size_t key_idx = 0;

    for (unsigned long long i = 0; i < (UINT_PTR)fileSize; i++)
    {
        original[i] ^= key[key_idx];
        key_idx = (key_idx + 1) % key.size();
    }

    return original;
}